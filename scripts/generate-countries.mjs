import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import worldCountries from "world-countries";

const EXTRA_PLAYABLE_CODES = new Set([
  "PSE",
  "TWN",
]);

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function toCountry(entry) {
  const code2 = entry.cca2.toLowerCase();
  const name = entry.translations?.spa?.common ?? entry.name.common;
  const englishName = entry.name.common;
  const officialName = entry.translations?.spa?.official ?? entry.name.official;

  return {
    name,
    englishName,
    officialName,
    code2,
    code3: entry.cca3,
    lat: entry.latlng[0],
    lng: entry.latlng[1],
    region: entry.region,
    subregion: entry.subregion ?? "",
    borders: entry.borders ?? [],
    aliases: unique([
      name,
      englishName,
      officialName,
      entry.name.official,
      ...(entry.altSpellings ?? []),
      entry.cca2,
      entry.cca3,
      entry.cioc,
    ]),
    flagPng: `https://flagcdn.com/w320/${code2}.png`,
    flagSvg: `https://flagcdn.com/${code2}.svg`,
  };
}

const countries = worldCountries
  .filter(entry => {
    const isPlayableCountry =
      entry.independent || entry.unMember || EXTRA_PLAYABLE_CODES.has(entry.cca3);

    return (
      isPlayableCountry &&
      entry.cca2 &&
      entry.cca3 &&
      Array.isArray(entry.latlng) &&
      entry.latlng.length === 2
    );
  })
  .map(toCountry)
  .sort((a, b) => a.name.localeCompare(b.name, "es"));

const output = `// Generated by scripts/generate-countries.mjs. Do not edit by hand.
const countries = ${JSON.stringify(countries, null, 2)};

export default countries;
`;

const outPath = fileURLToPath(new URL("../countries.generated.js", import.meta.url));
writeFileSync(outPath, output);
console.log(`Generated ${countries.length} countries.`);
